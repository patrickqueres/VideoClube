    private void LstEmprestimoValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_LstEmprestimoValueChanged
        // TODO add your handling code here:
        if (LstEmprestimo.getSelectedValue() == null) {
            return;
        }
        
        emprestimo = (Emprestimo) LstEmprestimo.getSelectedValue();
        txtValorC.setText(String.valueOf(emprestimo.getValorCobrado()));
        txtValorP.setText(String.valueOf(emprestimo.getValorPago()));
        txtID.setText(Integer.toString(emprestimo.getIdemprestimo()));
        txtDataE.setText(emprestimo.getDataEmprestimo());
        txtDataD.setText(emprestimo.getDataDevolucao());
        
        if(!emprestimo.getPago().equals(1) ) {
            chkPago.setSelected(false);
        } 
        else {
            chkPago.setSelected(true);
        }
                
       if(!emprestimo.getDevolvido().equals(1) ) {
            chkDevolvido.setSelected(false);
        } 
        else {
            chkDevolvido.setSelected(true);
        }
       
       cboSocio.setSelectedItem(emprestimo.getSocio());
       cboCopia.setSelectedItem(emprestimo.getCopia());
        
        
    }//GEN-LAST:event_LstEmprestimoValueChanged

