    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            // TODO add your handling code here:
            dao.daoGenero.excluir((Genero) LstGeneros.getSelectedValue());
            LstGeneros.setListData(dao.daoGenero.getGeneros().toArray());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }   
        
        txtSigla.setText("");
        txtNome.setText("");
        txtId.setText("");
        txtNome.requestFocus();
                
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void LstGenerosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_LstGenerosValueChanged
        // TODO add your handling code here:
        if (LstGeneros.getSelectedValue() == null) {
            return;
        }
        genero = (Genero) LstGeneros.getSelectedValue();
        txtNome.setText(genero.getNome());
        txtSigla.setText(genero.getSigla());
        txtId.setText(Integer.toString(genero.getId()));
        


    }//GEN-LAST:event_LstGenerosValueChanged

    private void btnAtualiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualiarActionPerformed
        // TODO add your handling code here:
        
        genero.setNome(txtNome.getText());
        genero.setSigla(txtSigla.getText());
        
        JOptionPane.showMessageDialog(null, "Registro atualizado!");
        
        
        try {
            dao.daoGenero.alterar(genero);
            LstGeneros.setListData(dao.daoGenero.getGeneros().toArray());
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }    
        
    }//GEN-LAST:event_btnAtualiarActionPerformed

